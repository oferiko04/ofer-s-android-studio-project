package com.example.finalproject;

public class picture {
    private String name;
    private String picture;

    public picture() {
    }

    public picture(String name, String picture) {
        this.name = name;
        this.picture = picture;
    }

    public String getName() {
        return name;
    }

    public String getPicture() {
        return picture;
    }
}